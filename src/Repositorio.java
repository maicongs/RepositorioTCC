
public class Repositorio {
	private String nome;
	private String email;
	private String curso;
	private String orientador;
	private String titulo;
	private String palavra_chave;
	private String resumo;
	private String situação;
	private String confirmação;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getCurso() {
		return curso;
	}
	public void setCurso(String curso) {
		this.curso = curso;
	}
	public String getOrientador() {
		return orientador;
	}
	public void setOrientador(String orientador) {
		this.orientador = orientador;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getPalavra_chave() {
		return palavra_chave;
	}
	public void setPalavra_chave(String palavra_chave) {
		this.palavra_chave = palavra_chave;
	}
	public String getResumo() {
		return resumo;
	}
	public void setResumo(String resumo) {
		this.resumo = resumo;
	}
	public String getSituação() {
		return situação;
	}
	public void setSituação(String situação) {
		this.situação = situação;
	}
	public String getConfirmação() {
		return confirmação;
	}
	public void setConfirmação(String confirmação) {
		this.confirmação = confirmação;
	}
	
	
	
	
	
}
